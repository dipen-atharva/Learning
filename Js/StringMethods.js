const name = "Prince Khant ";

// string length
console.log(name.length);

// string at()
console.log(name.at(0));

// string slice
console.log(name.slice(1, 4));

// String toUpperCase()
console.log(name.toUpperCase());

// String toLowerCase()
console.log(name.toLowerCase());

// String concat()
console.log(name.concat("How Are You?"));

// String trim()
console.log(name.trim());

//also String trimStart() and String trimEnd()

// String repeat()
console.log(name.repeat(2));

// String replace()
console.log(name.replace("e", "a"));

// String replaceAll()
console.log(name.replaceAll("happy"));

// String split()
console.log(name.split(""));

// String includes()
console.log(name.includes("Prince"));
